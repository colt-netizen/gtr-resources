#!/bin/bash
# Copy Skill Checker - Score your cold email like Lavender AI

cat << 'EOF'

╔════════════════════════════════════════════════════════════════╗
║         COLD EMAIL COPY CHECKER - Lavender Standard            ║
║                                                                ║
║  Score your email. 90+ = Ready to send. <70 = Rewrite.        ║
╚════════════════════════════════════════════════════════════════╝

CHECKLIST:

☐ WORD COUNT
  - Is it under 50 words? (Ideal: 25-50) 
  - Max 75 words
  - Every extra word = fewer replies
  Score: ___ / 20

☐ PERSONALIZATION  
  - Specific company/person detail (not generic)
  - Mention something THEY did (not your stuff)
  Score: ___ / 20

☐ PROBLEM FOCUS
  - Does it show understanding of their challenge?
  - Is it about THEIR problem, not your solution?
  - Would they think "yes, that's an issue"?
  Score: ___ / 15

☐ LANGUAGE QUALITY
  - 3rd-5th grade reading level?
  - No jargon (synergy, leverage, innovative)?
  - No clichés (hope this finds you, excited to connect)?
  - Simple and direct?
  Score: ___ / 20

☐ CALL TO ACTION
  - Is it a conversation starter (not a meeting ask)?
  - Easy to reply to?
  - Yes/no question or specific?
  - "Worth a chat?" or "How are you handling that?"
  Score: ___ / 15

☐ FORMATTING
  - White space (one idea per line)?
  - Mobile optimized (readable on phone)?
  - Subject line boring/internal sounding?
  Score: ___ / 10

────────────────────────────────────────────────────────────────

TOTAL SCORE: _____ / 100

90-100: SEND IT ✅ High reply probability
70-89:  GOOD → Make small edits
<70:    REWRITE ❌ Missing core elements

────────────────────────────────────────────────────────────────

QUICK REWRITES (If Score is Low):

Too long?
  → Cut 20% of words
  → Remove explanations
  → One sentence per idea

Not personal enough?
  → Add specific company detail (not role)
  → Reference something they DID
  → Show you researched them

Unclear problem?
  → Lead with observation
  → Connect to their challenge
  → "If X, then probably Y"

Sounds salesy?
  → Remove: "excited," "love to," "don't miss"
  → Remove: "limited time," "exclusive," "free"
  → Sound like them (peer, not vendor)

Too formal?
  → Shorten sentences
  → Remove: "I hope," "looking forward"
  → Sound like text to a friend

────────────────────────────────────────────────────────────────

FINAL CHECK BEFORE SENDING:

✓ Would I reply to this?
✓ Does it say something specific about THEM?
✓ Is there a real question (not a close)?
✓ Could a 7th grader understand it?
✓ Is it under 50 words?

If all YES → Send. If any NO → Fix it.

────────────────────────────────────────────────────────────────

Remember: Lavender data shows 40% reply rate comes from:
  1. Short (<50 words): 83% more replies
  2. Personalized: 45% reply rate
  3. Problem-focused: 3x vs feature-focused
  4. Simple language: 67% more replies
  5. Easy to reply: Yes/no not essays

Do these 5 right = You beat 98% of cold emailers.

EOF
